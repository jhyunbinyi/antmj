  For lack of better words, there will never be another player that shoots like Michael Jordan. There simply can’t be. The way players play now and the way they did back in MJ’s prime is just vastly different. After the emergence of three point shots, modern day offences base their game plans around scoring threes and as a result the stars of today like Anthony Edwards also base their game around scoring a lot of threes. This is reflected in the shot chart where you can see that the vast majority of Ants shots seem to come from beyond the arc, and almost all of MJ’s shots come from within it. This is what we hoped people would draw from our visualizations, Ant enjoys shooting a lot of threes and playing around the rim while MJ thrives in the mid-range. They’re different players and products of their eras. We also hoped that people would recognize how unrealistic it is to compare any young player to MJ. Per the bar chart, even a young MJ was averaging numbers that would put him among the top 3 players in a higher scoring, more efficient league. We hope that audiences understand that MJ was generational, and players like that appear once in a generation. Forcing comparisons like these sets modern players up for failure. 
